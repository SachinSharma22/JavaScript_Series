let num = 2;



switch(num) {
    case 1:
        console.log("This is One");
    break;
    case 2:
        console.log("This is two");
    break;
    case 3:
        console.log("This is Three");
    break;
    case 4:
        console.log("This is four");
    break;
    case 5:
        console.log("This is Five");
    break;
    case 6:
        console.log("This is six");
    break;
    default:
        console.log("Enter a valid number");
    break;
}